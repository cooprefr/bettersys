def parse_wallets():
    wallets = {}
    invalid_count = 0
    
    for line in WALLET_DATA.strip().split('\n'):
        if ',' in line:
            parts = line.strip().split(',', 1)
            if len(parts) == 2:
                addr, label = parts
                addr = addr.lower().strip()
                label = label.strip().lower().replace(' ', '_')
                
                # Skip invalid wallet address (wrong length)
                if len(addr) == 43 and addr.endswith('a'):
                    # This is the malformed 0xd774bc7dcec973281ac8d8a08bef1827b66d9cb8a
                    addr = addr[:-1]  # Remove the extra 'a'
                
                if len(addr) == 42 and addr.startswith('0x'):
                    wallets[addr] = label
                else:
                    invalid_count += 1
                    print(f"Invalid wallet: {addr} (length={len(addr)})")
    
    return wallets, invalid_count

def generate_rust_code(wallets):
    # Group by category
    categories = {}
    for addr, label in sorted(wallets.items(), key=lambda x: (x[1], x[0])):
        if label not in categories:
            categories[label] = []
        categories[label].append(addr)
    
    # Generate Rust code
    lines = []
    lines.append("    /// Verified insider wallet list - auto-generated")
    lines.append("    /// Total: {} unique wallets".format(len(wallets)))
    lines.append("    pub fn default_tracked_wallets() -> std::collections::HashMap<String, String> {")
    lines.append("        let mut wallets = std::collections::HashMap::with_capacity({});".format(len(wallets)))
    lines.append("")
    
    # Add high-frequency test wallet first
    lines.append("        // HIGH FREQUENCY TEST WALLET - for WebSocket verification")
    lines.append('        wallets.insert("0x6031b6eed1c97e853c6e0f03ad3ce3529351f96d".to_string(), "high_frequency_test".to_string());')
    lines.append("")
    
    for category in sorted(categories.keys()):
        addrs = categories[category]
        lines.append(f"        // {category.upper().replace('_', ' ')} ({len(addrs)} wallets)")
        for addr in addrs:
            lines.append(f'        wallets.insert("{addr}".to_string(), "{category}".to_string());')
        lines.append("")
    
    lines.append("        wallets")
    lines.append("    }")
    
    return '\n'.join(lines)

